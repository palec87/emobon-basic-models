"""Evaluation helpers for MLflow LOOCV modeling outputs."""

from __future__ import annotations

from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
import re
from typing import Any

import numpy as np
import pandas as pd


_RUN_ID_PATTERN = re.compile(r"^[0-9a-f]{32}$")


@dataclass(slots=True)
class RunArtifacts:
    """Container for artifacts loaded from a single MLflow run."""

    run_id: str
    experiment_id: str
    run_dir: Path
    fold_metrics: pd.DataFrame
    predictions: pd.DataFrame
    feature_importances: pd.DataFrame


def _is_run_dir(path: Path) -> bool:
    """Return True when a directory looks like an MLflow run folder."""
    return (
        path.is_dir()
        and _RUN_ID_PATTERN.match(path.name) is not None
        and (path / "meta.yaml").exists()
    )


def _experiment_dirs(mlruns_dir: Path) -> list[Path]:
    """Return all experiment directories in an MLflow root directory."""
    if not mlruns_dir.exists():
        msg = f"mlruns directory not found: {mlruns_dir}"
        raise FileNotFoundError(msg)

    return sorted(
        [
            path
            for path in mlruns_dir.iterdir()
            if path.is_dir()
            and path.name != ".trash"
            and path.name.isdigit()
        ]
    )


def _collect_run_dirs(
    mlruns_dir: Path,
    experiment_id: str | None,
) -> list[Path]:
    """Collect candidate run directories from mlruns for selection."""
    if experiment_id is not None:
        exp_dir = mlruns_dir / experiment_id
        if not exp_dir.exists():
            msg = f"Experiment directory not found: {exp_dir}"
            raise FileNotFoundError(msg)
        return [path for path in exp_dir.iterdir() if _is_run_dir(path)]

    run_dirs: list[Path] = []
    for exp_dir in _experiment_dirs(mlruns_dir):
        run_dirs.extend(
            path for path in exp_dir.iterdir() if _is_run_dir(path)
        )
    return run_dirs


def select_run_dir(
    mlruns_dir: Path,
    run_id: str | None = None,
    experiment_id: str | None = None,
) -> Path:
    """Select an MLflow run directory by run ID or latest modified time."""
    candidate_dirs = _collect_run_dirs(mlruns_dir, experiment_id)
    if not candidate_dirs:
        msg = "No MLflow run directories found"
        raise FileNotFoundError(msg)

    if run_id is not None:
        for candidate in candidate_dirs:
            if candidate.name == run_id:
                return candidate
        msg = f"Run ID not found in selected scope: {run_id}"
        raise FileNotFoundError(msg)

    return max(candidate_dirs, key=lambda path: path.stat().st_mtime)


def _required_columns(df: pd.DataFrame, columns: list[str]) -> None:
    """Validate required columns in a dataframe."""
    missing = [column for column in columns if column not in df.columns]
    if missing:
        msg = f"Missing required columns: {missing}"
        raise ValueError(msg)


def load_run_artifacts(run_dir: Path) -> RunArtifacts:
    """Load run artifacts generated by ``run_group_loocv_with_mlflow``."""
    artifacts_dir = run_dir / "artifacts"

    fold_metrics_path = artifacts_dir / "fold_metrics.csv"
    predictions_path = artifacts_dir / "fold_predictions.csv"
    feature_importances_path = artifacts_dir / "feature_importances.csv"

    if not fold_metrics_path.exists():
        msg = f"Missing artifact: {fold_metrics_path}"
        raise FileNotFoundError(msg)
    if not predictions_path.exists():
        msg = f"Missing artifact: {predictions_path}"
        raise FileNotFoundError(msg)
    if not feature_importances_path.exists():
        msg = f"Missing artifact: {feature_importances_path}"
        raise FileNotFoundError(msg)

    fold_metrics = pd.read_csv(fold_metrics_path)
    predictions = pd.read_csv(predictions_path)
    feature_importances = pd.read_csv(feature_importances_path)

    _required_columns(
        fold_metrics,
        ["fold", "group", "n_train", "n_test", "mae", "rmse"],
    )
    has_sample_id = "sample_id" in predictions.columns
    has_legacy_index = "index" in predictions.columns
    if not has_sample_id and has_legacy_index:
        predictions = predictions.rename(columns={"index": "sample_id"})

    _required_columns(predictions, ["sample_id", "fold", "group"])
    _required_columns(feature_importances, ["feature", "importance"])

    true_columns = [
        column for column in predictions.columns if column.startswith("true__")
    ]
    pred_columns = [
        column for column in predictions.columns if column.startswith("pred__")
    ]
    if not true_columns or not pred_columns:
        msg = "Prediction artifact must include true__* and pred__* columns"
        raise ValueError(msg)

    return RunArtifacts(
        run_id=run_dir.name,
        experiment_id=run_dir.parent.name,
        run_dir=run_dir,
        fold_metrics=fold_metrics,
        predictions=predictions,
        feature_importances=feature_importances,
    )


def evaluate_train_test_splits(fold_metrics: pd.DataFrame) -> dict[str, Any]:
    """Evaluate train-test split integrity using fold metrics artifact."""
    _required_columns(fold_metrics, ["fold", "group", "n_train", "n_test"])

    grouped = fold_metrics.groupby("group", dropna=False).size()
    duplicate_groups = grouped[grouped > 1].index.astype(str).tolist()

    invalid_train = fold_metrics.loc[
        fold_metrics["n_train"] <= 0,
        "fold",
    ].tolist()
    invalid_test = fold_metrics.loc[
        fold_metrics["n_test"] <= 0,
        "fold",
    ].tolist()

    expected_total = (
        fold_metrics["n_train"].astype(int)
        + fold_metrics["n_test"].astype(int)
    )
    consistent_total = bool(expected_total.nunique() == 1)

    fold_table = fold_metrics.copy()
    fold_table["test_ratio"] = (
        fold_table["n_test"].astype(float)
        /
        (
            fold_table["n_train"].astype(float)
            + fold_table["n_test"].astype(float)
        )
    )

    return {
        "n_folds": int(fold_metrics.shape[0]),
        "unique_groups": int(fold_metrics["group"].nunique(dropna=False)),
        "duplicate_groups": duplicate_groups,
        "invalid_train_folds": invalid_train,
        "invalid_test_folds": invalid_test,
        "consistent_total_samples": consistent_total,
        "total_samples": (
            int(expected_total.iloc[0]) if consistent_total else None
        ),
        "fold_table": fold_table.sort_values("fold").reset_index(drop=True),
    }


def to_long_predictions(predictions: pd.DataFrame) -> pd.DataFrame:
    """Convert wide true/pred columns to long format with residuals."""
    _required_columns(predictions, ["sample_id", "fold", "group"])

    true_columns = [
        column for column in predictions.columns if column.startswith("true__")
    ]
    if not true_columns:
        msg = "No true__ columns found in predictions"
        raise ValueError(msg)

    rows: list[pd.DataFrame] = []
    for true_column in true_columns:
        taxon = true_column.removeprefix("true__")
        pred_column = f"pred__{taxon}"
        if pred_column not in predictions.columns:
            continue

        aligned = predictions[
            ["sample_id", "fold", "group", true_column, pred_column]
        ].rename(columns={true_column: "y_true", pred_column: "y_pred"})
        aligned["taxon"] = taxon
        rows.append(aligned)

    if not rows:
        msg = "No matched true/pred column pairs found"
        raise ValueError(msg)

    long_df = pd.concat(rows, axis=0, ignore_index=True)
    long_df["residual"] = long_df["y_true"] - long_df["y_pred"]
    long_df["abs_error"] = long_df["residual"].abs()
    return long_df


def top_taxa_for_plotting(
    long_predictions: pd.DataFrame,
    top_n: int,
) -> list[str]:
    """Select taxa with the largest true-value variance for plotting."""
    if top_n <= 0:
        return []
    variance = (
        long_predictions.groupby("taxon", dropna=False)["y_true"]
        .var(ddof=0)
        .sort_values(ascending=False)
    )
    return variance.head(top_n).index.astype(str).tolist()


def default_plot_output_dir(root_dir: Path) -> Path:
    """Build timestamped plot output directory under outputs/."""
    timestamp = datetime.now().strftime("%Y%m%d_%H%M")
    return root_dir / "outputs" / f"analysis_{timestamp}" / "plots"


def write_summary_report(
    output_dir: Path,
    run_artifacts: RunArtifacts,
    split_report: dict[str, Any],
    long_predictions: pd.DataFrame,
) -> Path:
    """Write a compact markdown report with key split and error findings."""
    output_dir.mkdir(parents=True, exist_ok=True)

    rmse_by_group = (
        run_artifacts.fold_metrics.groupby("group", dropna=False)["rmse"]
        .mean()
        .sort_values(ascending=False)
    )
    mae_by_group = (
        run_artifacts.fold_metrics.groupby("group", dropna=False)["mae"]
        .mean()
        .sort_values(ascending=False)
    )
    median_abs_error = float(long_predictions["abs_error"].median())

    lines = [
        "# Modeling Evaluation Summary",
        "",
        f"Run ID: `{run_artifacts.run_id}`",
        f"Experiment ID: `{run_artifacts.experiment_id}`",
        "",
        "## Split Integrity",
        f"- Number of folds: {split_report['n_folds']}",
        f"- Unique held-out groups: {split_report['unique_groups']}",
        (
            "- Duplicate held-out groups: "
            f"{split_report['duplicate_groups'] or 'None'}"
        ),
        (
            "- Invalid train folds (n_train <= 0): "
            f"{split_report['invalid_train_folds'] or 'None'}"
        ),
        (
            "- Invalid test folds (n_test <= 0): "
            f"{split_report['invalid_test_folds'] or 'None'}"
        ),
        (
            "- Consistent total samples per fold: "
            f"{split_report['consistent_total_samples']}"
        ),
        "",
        "## Error Overview",
        (
            "- Mean MAE across folds: "
            f"{float(run_artifacts.fold_metrics['mae'].mean()):.6f}"
        ),
        (
            "- Mean RMSE across folds: "
            f"{float(run_artifacts.fold_metrics['rmse'].mean()):.6f}"
        ),
        f"- Median absolute residual (sample-level): {median_abs_error:.6f}",
        "",
        "## Hardest Groups by RMSE",
    ]

    for group_name, rmse in rmse_by_group.head(3).items():
        mae = float(mae_by_group.loc[group_name])
        lines.append(f"- {group_name}: RMSE={rmse:.6f}, MAE={mae:.6f}")

    summary_path = output_dir / "summary.md"
    summary_path.write_text("\n".join(lines), encoding="utf-8")
    return summary_path


def _get_pyplot_module() -> Any:
    """Import matplotlib.pyplot lazily with a clear error message."""
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        msg = "matplotlib is required for evaluation plotting"
        raise ImportError(msg) from exc
    return plt


def plot_fold_sizes(fold_metrics: pd.DataFrame, output_path: Path) -> None:
    """Plot train and test sample counts for each held-out group fold."""
    plt = _get_pyplot_module()

    ordered = fold_metrics.sort_values("fold")
    x_labels = [f"{row.fold}: {row.group}" for row in ordered.itertuples()]
    positions = np.arange(len(ordered))
    width = 0.4

    fig, ax = plt.subplots(figsize=(12, 6))
    ax.bar(
        positions - width / 2,
        ordered["n_train"],
        width=width,
        label="n_train",
        color="#1f77b4",
    )
    ax.bar(
        positions + width / 2,
        ordered["n_test"],
        width=width,
        label="n_test",
        color="#ff7f0e",
    )
    ax.set_xticks(positions)
    ax.set_xticklabels(x_labels, rotation=45, ha="right")
    ax.set_ylabel("Sample count")
    ax.set_title("Train/Test Sample Counts by LOOCV Fold")
    ax.legend()
    fig.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)


def plot_metric_by_group(
    fold_metrics: pd.DataFrame,
    metric: str,
    output_path: Path,
) -> None:
    """Plot fold metric values by held-out group."""
    if metric not in {"mae", "rmse"}:
        msg = "metric must be either 'mae' or 'rmse'"
        raise ValueError(msg)

    plt = _get_pyplot_module()
    ordered = fold_metrics.sort_values(metric, ascending=False)

    fig, ax = plt.subplots(figsize=(11, 6))
    ax.bar(ordered["group"].astype(str), ordered[metric], color="#2ca02c")
    ax.set_ylabel(metric.upper())
    ax.set_xlabel("Held-out group")
    ax.set_title(f"{metric.upper()} by Held-out Group")
    ax.tick_params(axis="x", rotation=45)
    fig.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)


def plot_true_vs_pred(
    long_predictions: pd.DataFrame,
    output_path: Path,
    taxa: list[str] | None = None,
) -> None:
    """Plot true versus predicted values with identity reference line."""
    plt = _get_pyplot_module()

    if taxa:
        plot_df = long_predictions[long_predictions["taxon"].isin(taxa)]
    else:
        plot_df = long_predictions

    x = plot_df["y_true"].to_numpy()
    y = plot_df["y_pred"].to_numpy()
    lower = float(min(x.min(), y.min()))
    upper = float(max(x.max(), y.max()))

    fig, ax = plt.subplots(figsize=(7, 7))
    ax.scatter(x, y, alpha=0.35, s=10, color="#1f77b4")
    ax.plot([lower, upper], [lower, upper], linestyle="--", color="#d62728")
    ax.set_xlabel("True abundance")
    ax.set_ylabel("Predicted abundance")
    ax.set_title("True vs Predicted Abundances")
    fig.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)


def plot_residual_distribution(
    long_predictions: pd.DataFrame,
    output_path: Path,
) -> None:
    """Plot histogram of residual values over all sample-level predictions."""
    plt = _get_pyplot_module()

    residuals = long_predictions["residual"].to_numpy()
    fig, ax = plt.subplots(figsize=(10, 6))
    ax.hist(residuals, bins=40, color="#9467bd", alpha=0.8)
    ax.axvline(0.0, color="#111111", linestyle="--", linewidth=1)
    ax.set_xlabel("Residual (true - predicted)")
    ax.set_ylabel("Count")
    ax.set_title("Residual Distribution")
    fig.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)


def plot_feature_importances(
    feature_importances: pd.DataFrame,
    output_path: Path,
    top_k: int,
) -> None:
    """Plot top-k features ranked by model importance values."""
    if top_k <= 0:
        msg = "top_k must be greater than zero"
        raise ValueError(msg)

    plt = _get_pyplot_module()
    top_df = feature_importances.sort_values(
        "importance",
        ascending=False,
    ).head(top_k)
    top_df = top_df.iloc[::-1]

    fig, ax = plt.subplots(figsize=(10, 8))
    ax.barh(
        top_df["feature"].astype(str),
        top_df["importance"],
        color="#8c564b",
    )
    ax.set_xlabel("Importance")
    ax.set_title(f"Top {top_k} Feature Importances")
    fig.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)
